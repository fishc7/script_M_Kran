def check_grouping_issue():
    """Проверка группировки - сколько записей с 'годен' попадает в каждую группу"""
    conn = get_database_connection()
    c = conn.cursor()
    
    print("=" * 60)
    print("ДЕТАЛЬНАЯ ПРОВЕРКА: Группировка и подсчет 'годен_стилоскопирование'")
    print("=" * 60)
    
    # Проверим несколько конкретных примеров
    c.execute("""
        SELECT 
            ss.id,
            ss.N_Линии,
            ss._Линия,
            ss.Номер_чертежа,
            ss.Всего_сваренно_сварщиком,
            ss.не_годен_рк,
            ss.годен_стилоскопирование,
            ss."Стило(PMI)",
            ss.заявлен_стилоскопирование,
            -- Проверяем сколько реально записей с 'годен' в wl_china для ЭТОЙ ГРУППЫ (с учетом клейм!)
            -- Важно: нужно проверить, что запись принадлежит к той же группе клейм
            (SELECT COUNT(*)
             FROM wl_china wc
             WHERE wc.N_Линии = ss.N_Линии
               AND wc.Номер_чертежа = ss.Номер_чертежа
               AND wc.Дата_сварки IS NOT NULL AND TRIM(wc.Дата_сварки) <> ''
               AND (
                   (wc.Клеймо_сварщика_корневой_слой IS NOT NULL AND wc.Клеймо_сварщика_корневой_слой <> '')
                   OR (wc.Клеймо_сварщика_заполнение_облицовка IS NOT NULL AND wc.Клеймо_сварщика_заполнение_облицовка <> '')
               )
               AND wc.Результаты_Заключения_Стилоскопирование = 'годен'
               -- Проверка принадлежности к той же группе клейм:
               AND (
                   -- Клеймо корневого слоя входит в строку клейм группы
                   (ss.клейма_сварщиков LIKE '%' || TRIM(wc.Клеймо_сварщика_корневой_слой) || '%'
                    AND wc.Клеймо_сварщика_корневой_слой IS NOT NULL AND wc.Клеймо_сварщика_корневой_слой <> '')
                   OR 
                   -- Клеймо заполнения входит в строку клейм группы
                   (ss.клейма_сварщиков LIKE '%' || TRIM(wc.Клеймо_сварщика_заполнение_облицовка) || '%'
                    AND wc.Клеймо_сварщика_заполнение_облицовка IS NOT NULL AND wc.Клеймо_сварщика_заполнение_облицовка <> '')
               )
            ) as реальных_годен_в_группе,
            -- Проверяем клейма сварщиков
            ss.клейма_сварщиков
        FROM сварено_сварщиком ss
        WHERE ss."Стило(PMI)" IS NOT NULL
          AND ss.годен_стилоскопирование > 0
        ORDER BY ss.годен_стилоскопирование DESC
        LIMIT 10
    """)
    
    results = c.fetchall()
    
    print(f"\nНайдено групп с годен_стилоскопирование > 0: {len(results)}\n")
    
    mismatches = 0
    for row in results:
        id_val, n_линии, _линия, чертеж, всего, не_годен_рк, годен, стило, заявлен, реальных_годен, клейма = row
        
        if годен != реальных_годен:
            mismatches += 1
            print(f"Группа ID {id_val}:")
            print(f"  Линия: {n_линии} (_Линия: {_линия})")
            print(f"  Чертеж: {чертеж}")
            print(f"  Клейма: {клейма}")
            print(f"  Всего сварено: {всего}")
            print(f"  не_годен_рк: {не_годен_рк}")
            print(f"  годен_стилоскопирование (в таблице): {годен}")
            print(f"  Реальных 'годен' в этой группе (с учетом клейм): {реальных_годен}")
            print(f"  Стило(PMI): {стило}")
            print(f"  заявлен_стилоскопирование: {заявлен}")
            
            if годен != реальных_годен:
                print(f"  !!! ВНИМАНИЕ: Разница = {abs(годен - реальных_годен)}")
                print(f"     (Это может быть из-за сложной логики группировки клейм)")
            else:
                print(f"  OK: Значения совпадают")
            
            # Проверим что происходит с группировкой
            c.execute("""
                SELECT 
                    COUNT(*) as всего_записей,
                    COUNT(CASE WHEN wc.Результаты_Заключения_Стилоскопирование = 'годен' THEN 1 END) as годен_записей,
                    COUNT(DISTINCT wc.Клеймо_сварщика_корневой_слой || '/' || COALESCE(wc.Клеймо_сварщика_заполнение_облицовка, '')) as уникальных_клейм
                FROM wl_china wc
                WHERE wc.N_Линии = ?
                  AND wc.Номер_чертежа = ?
                  AND wc.Дата_сварки IS NOT NULL AND TRIM(wc.Дата_сварки) <> ''
                  AND (
                      (wc.Клеймо_сварщика_корневой_слой IS NOT NULL AND wc.Клеймо_сварщика_корневой_слой <> '')
                      OR (wc.Клеймо_сварщика_заполнение_облицовка IS NOT NULL AND wc.Клеймо_сварщика_заполнение_облицовка <> '')
                  )
            """, (n_линии, чертеж))
            
            detail = c.fetchone()
            print(f"  Детали из wl_china для этой линии/чертежа:")
            print(f"    Всего записей: {detail[0]}")
            print(f"    Записей с 'годен': {detail[1]}")
            print(f"    Уникальных комбинаций клейм: {detail[2]}")
            print()
    
    if mismatches == 0:
        print("OK: Все значения годен_стилоскопирование соответствуют данным в wl_china для своих групп")
    else:
        print(f"\nВНИМАНИЕ: Обнаружено {mismatches} групп с различиями")
        print("Примечание: Различия могут быть из-за:")
        print("  1. Сложной логики объединения клейм (group_concat)")
        print("  2. Разных комбинаций клейм в одной группе")
        print("  3. Нужно проверять точное соответствие группировке из SQL")
    
    conn.close()

def check_pmi_vs_goden():
    """Проверка соответствия Стило(PMI) с учетом годен_стилоскопирование"""
    conn = get_database_connection()
    c = conn.cursor()
    
    print("\n" + "=" * 60)
    print("ПРОВЕРКА: Стило(PMI) как остаточное плановое значение")
    print("=" * 60)
    
    # Проверим логику: Стило(PMI) должно быть = план - годен_стилоскопирование
    c.execute("""
        SELECT 
            ss.id,
            ss._Линия,
            ss.Всего_сваренно_сварщиком,
            ss.не_годен_рк,
            ss.годен_стилоскопирование,
            ss."Стило(PMI)",
            ss.заявлен_стилоскопирование,
            он."СТ (Стилоскоп) Стилоскопирование / PMI" as процент,
            -- Расчет плана
            CASE 
                WHEN EXISTS (
                    SELECT 1 FROM основнаяНК он2
                    WHERE он2."Номер линии / Line No" = ss._Линия
                      AND он2."СТ (Стилоскоп) Стилоскопирование / PMI" LIKE '%*%'
                ) THEN 2
                ELSE CAST((
                    (MAX(ss.Всего_сваренно_сварщиком - ss.не_годен_рк, 0)) * 
                    COALESCE(CAST(REPLACE(REPLACE(он."СТ (Стилоскоп) Стилоскопирование / PMI", '%', ''), ',', '.') AS REAL), 0) + 99
                ) / 100 AS INT)
            END as план_до_вычета,
            -- Ожидаемое Стило(PMI) = план - годен
            CASE 
                WHEN EXISTS (
                    SELECT 1 FROM основнаяНК он2
                    WHERE он2."Номер линии / Line No" = ss._Линия
                      AND он2."СТ (Стилоскоп) Стилоскопирование / PMI" LIKE '%*%'
                ) THEN MAX(2 - COALESCE(ss.годен_стилоскопирование, 0), 0)
                ELSE MAX(CAST((
                    (MAX(ss.Всего_сваренно_сварщиком - ss.не_годен_рк, 0)) * 
                    COALESCE(CAST(REPLACE(REPLACE(он."СТ (Стилоскоп) Стилоскопирование / PMI", '%', ''), ',', '.') AS REAL), 0) + 99
                ) / 100 AS INT) - COALESCE(ss.годен_стилоскопирование, 0), 0)
            END as ожидаемое_стило
        FROM сварено_сварщиком ss
        LEFT JOIN основнаяНК он ON он."Номер линии / Line No" = ss._Линия
        WHERE ss."Стило(PMI)" IS NOT NULL
          AND ss.годен_стилоскопирование > 0
        ORDER BY ABS(ss."Стило(PMI)" - (
            CASE 
                WHEN EXISTS (
                    SELECT 1 FROM основнаяНК он2
                    WHERE он2."Номер линии / Line No" = ss._Линия
                      AND он2."СТ (Стилоскоп) Стилоскопирование / PMI" LIKE '%*%'
                ) THEN MAX(2 - COALESCE(ss.годен_стилоскопирование, 0), 0)
                ELSE MAX(CAST((
                    (MAX(ss.Всего_сваренно_сварщиком - ss.не_годен_рк, 0)) * 
                    COALESCE(CAST(REPLACE(REPLACE(он."СТ (Стилоскоп) Стилоскопирование / PMI", '%', ''), ',', '.') AS REAL), 0) + 99
                ) / 100 AS INT) - COALESCE(ss.годен_стилоскопирование, 0), 0)
            END
        )) DESC
        LIMIT 10
    """)
    
    results = c.fetchall()
    
    print("\nПримеры записей с годен_стилоскопирование > 0:\n")
    
    for row in results:
        id_val, линия, всего, не_годен_рк, годен, стило, заявлен, процент, план_до, ожидаемое = row
        
        print(f"ID {id_val}, Линия {линия}:")
        print(f"  Всего: {всего}, не_годен_рк: {не_годен_рк}")
        print(f"  годен_стилоскопирование: {годен}")
        print(f"  Процент из основнойНК: {процент}")
        print(f"  План (до вычета годен): {план_до}")
        print(f"  Ожидаемое Стило(PMI) = план - годен: {ожидаемое}")
        print(f"  Текущее Стило(PMI): {стило}")
        print(f"  заявлен_стилоскопирование: {заявлен}")
        
        if стило != ожидаемое:
            print(f"  !!! НЕСООТВЕТСТВИЕ! Разница: {abs(стило - ожидаемое)}")
        else:
            print(f"  OK")
        print()
    
    conn.close()

